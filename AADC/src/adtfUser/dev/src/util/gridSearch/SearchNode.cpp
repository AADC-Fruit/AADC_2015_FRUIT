#include "SearchNode.h"

// -------------------------------------------------------------------------------------------------
SearchNode::SearchNode(Vector2 coordinates, int cost, int path_length, SearchNode * parent, int action) {
// -------------------------------------------------------------------------------------------------
    coordinates_ = coordinates;
    cost_ = cost;
    path_length_ = path_length;
    parent_ = parent;
    action_ = action;
}

// -------------------------------------------------------------------------------------------------
Vector2 SearchNode::get_coordinates() const {
// -------------------------------------------------------------------------------------------------
    return coordinates_;
}

// -------------------------------------------------------------------------------------------------
int SearchNode::get_cost() const {
// -------------------------------------------------------------------------------------------------
    return cost_;
}

// -------------------------------------------------------------------------------------------------
int SearchNode::get_path_length() const {
// -------------------------------------------------------------------------------------------------
    return path_length_;
}

// -------------------------------------------------------------------------------------------------
SearchNode * SearchNode::get_parent() const {
// -------------------------------------------------------------------------------------------------
    return parent_;
}

// -------------------------------------------------------------------------------------------------
int SearchNode::get_action() const {
// -------------------------------------------------------------------------------------------------
    return action_;
}
