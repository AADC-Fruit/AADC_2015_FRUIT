#include "media_data.h"

// -------------------------------------------------------------------------------------------------
MediaData::MediaData(int integer_data, std::string string_data) {
// -------------------------------------------------------------------------------------------------
    integer_data_ = integer_data;
    string_data_ = string_data;
}

// -------------------------------------------------------------------------------------------------
int MediaData::get_integer_data() const {
// -------------------------------------------------------------------------------------------------
    return integer_data_;
}

// -------------------------------------------------------------------------------------------------
std::string MediaData::get_string_data() const {
// -------------------------------------------------------------------------------------------------
    return string_data_;
}