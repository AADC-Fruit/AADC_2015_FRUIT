#include "line_triple.h"
#include <assert.h>

// -------------------------------------------------------------------------------------------------
LineTriple::LineTriple(std::vector<LPoint> leftLine, std::vector<LPoint> middleLine, std::vector<LPoint> rightLine) {
// -------------------------------------------------------------------------------------------------
  left_line_ = leftLine;
  middle_line_ = middleLine;
  right_line_ = rightLine;
}

// -------------------------------------------------------------------------------------------------
std::vector<LPoint> LineTriple::get_left() const {
// -------------------------------------------------------------------------------------------------
  return left_line_;
}

// -------------------------------------------------------------------------------------------------
std::vector<LPoint> LineTriple::get_mid() const {
// -------------------------------------------------------------------------------------------------
  return middle_line_;
}

// -------------------------------------------------------------------------------------------------
std::vector<LPoint> LineTriple::get_right() const {
// -------------------------------------------------------------------------------------------------
  return right_line_;
}

// -------------------------------------------------------------------------------------------------
void LineTriple::set_left(std::vector<LPoint> leftLine) {
// -------------------------------------------------------------------------------------------------
  left_line_ = leftLine;
}

// -------------------------------------------------------------------------------------------------
void LineTriple::set_mid(std::vector<LPoint> middleLine) {
// -------------------------------------------------------------------------------------------------
  middle_line_ = middleLine;
}

// -------------------------------------------------------------------------------------------------
void LineTriple::set_right(std::vector<LPoint> rightLine) {
// -------------------------------------------------------------------------------------------------
  right_line_ = rightLine;
}
