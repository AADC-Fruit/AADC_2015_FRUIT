#include "l_point.h"
#include <assert.h>

// -------------------------------------------------------------------------------------------------
LPoint::LPoint() {
// -------------------------------------------------------------------------------------------------
  x1_ = 0;
  x2_ = 0;
  y_ = 0;
}

// -------------------------------------------------------------------------------------------------
LPoint::LPoint(int x1, int x2, int y) {
// -------------------------------------------------------------------------------------------------
  x1_ = x1;
  x2_ = x2;
  y_ = y;
}

// -------------------------------------------------------------------------------------------------
LPoint::LPoint(LPoint const & other) {
// -------------------------------------------------------------------------------------------------
  x1_ = other.get_x1();
  x2_ = other.get_x2();
  y_ = other.get_y();
}

// -------------------------------------------------------------------------------------------------
int LPoint::operator[](unsigned int index) const {
// -------------------------------------------------------------------------------------------------
  if (index == 0) return get_x1();
  else if (index == 1) return get_x2();
  else if (index == 2) return get_y();
  return -1;
}

// -------------------------------------------------------------------------------------------------
int LPoint::get_x1() const {
// -------------------------------------------------------------------------------------------------
  return x1_;
}

// -------------------------------------------------------------------------------------------------
int LPoint::get_x2() const {
// -------------------------------------------------------------------------------------------------
  return x2_;
}

// -------------------------------------------------------------------------------------------------
int LPoint::get_y() const {
// -------------------------------------------------------------------------------------------------
  return y_;
}

// -------------------------------------------------------------------------------------------------
int LPoint::get_mean_x() const {
// -------------------------------------------------------------------------------------------------
  return (int)((x1_ + x2_)/2.0);
}
