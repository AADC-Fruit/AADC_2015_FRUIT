#include "object.h"

// -------------------------------------------------------------------------------------------------
Object::Object() {
// -------------------------------------------------------------------------------------------------
  x_ = ERROR_VALUE;
  y_ = ERROR_VALUE;
  width_ = ERROR_VALUE;
  height_ = ERROR_VALUE;
}

// -------------------------------------------------------------------------------------------------
Object::Object(int x, int y, int width, int height, int source_width, int source_height) {
// -------------------------------------------------------------------------------------------------
  x_ = x;
  y_ = y;
  width_ = width;
  height_ = height;
  source_width_ = source_width;
  source_height_ = source_height;
}

// -------------------------------------------------------------------------------------------------
int Object::get_absolute_x() const {
// -------------------------------------------------------------------------------------------------
  return x_;
}

// -------------------------------------------------------------------------------------------------
int Object::get_absolute_y() const {
// -------------------------------------------------------------------------------------------------
  return y_;
}

// -------------------------------------------------------------------------------------------------
int Object::get_absolute_width() const {
// -------------------------------------------------------------------------------------------------
  return width_;
}

// -------------------------------------------------------------------------------------------------
int Object::get_absolute_height() const {
// -------------------------------------------------------------------------------------------------
  return height_;
}

// -------------------------------------------------------------------------------------------------
double Object::get_relative_x() const {
// -------------------------------------------------------------------------------------------------
  return (double)x_ / source_width_;
}

// -------------------------------------------------------------------------------------------------
int Object::get_relative_x(int actual_width) const {
// -------------------------------------------------------------------------------------------------
  return get_relative_x() * actual_width;
}

// -------------------------------------------------------------------------------------------------
double Object::get_relative_y() const {
// -------------------------------------------------------------------------------------------------
  return (double)y_ / source_height_;
}

// -------------------------------------------------------------------------------------------------
int Object::get_relative_y(int actual_height) const {
// -------------------------------------------------------------------------------------------------
  return get_relative_y() * actual_height;
}

// -------------------------------------------------------------------------------------------------
double Object::get_relative_width() const {
// -------------------------------------------------------------------------------------------------
  return (double)width_ / source_width_;
}

// -------------------------------------------------------------------------------------------------
int Object::get_relative_width(int actual_width) const {
// -------------------------------------------------------------------------------------------------
  return get_relative_width() * actual_width;
}

// -------------------------------------------------------------------------------------------------
double Object::get_relative_height() const {
// -------------------------------------------------------------------------------------------------
  return (double)height_ / source_height_;
}

// -------------------------------------------------------------------------------------------------
int Object::get_relative_height(int actual_height) const {
// -------------------------------------------------------------------------------------------------
  return get_relative_height() * actual_height;
}
